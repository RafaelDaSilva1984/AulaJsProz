console.log("Aula de Diferenciação entre Var, Let e Const");
    
//Exemplo de como declarar let
if (true){
    let num = 10
    console.log("Declarando Variáveis com Let = ", num)
}
//Exemplo de como declarar var
if (true){
    var num = 10    
}
console.log("Declarando Variáveis com Var = ", num)

//Exemplo de como declarar const
if (true){
    const num = 10
    console.log(`Declarando Variáveis com Constante const = `, num)
}